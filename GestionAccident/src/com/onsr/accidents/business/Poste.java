package com.onsr.accidents.business;

import java.util.Date;

public class Poste {
	
	private int codep ;
	private String ville ;
	private String cite;
	private String province;
	private int nbremp ;
	private String prespos ;
	private String date ;
	public int getCodep() {
		return codep;
	}
	public void setCodep(int codep) {
		this.codep = codep;
	}
	public String getVille() {
		return ville;
	}
	public void setVille(String ville) {
		this.ville = ville;
	}
	public String getCite() {
		return cite;
	}
	public void setCite(String cite) {
		this.cite = cite;
	}
	public String getProvince() {
		return province;
	}
	public void setProvince(String province) {
		this.province = province;
	}
	public int getNbremp() {
		return nbremp;
	}
	public void setNbremp(int nbremp) {
		this.nbremp = nbremp;
	}
	public String getPrespos() {
		return prespos;
	}
	public void setPrespos(String prespos) {
		this.prespos = prespos;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
	

}
